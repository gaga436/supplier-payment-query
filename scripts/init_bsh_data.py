def init_db():
    if os.path.exists(DB_PATH):
        os.remove(DB_PATH)
    
    os.makedirs(os.path.dirname(DB_PATH), exist_ok=True)
    
    conn = sqlite3.connect(DB_PATH)
    c = conn.cursor()
    
    c.execute('''
        CREATE TABLE IF NOT EXISTS payments (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            付款编号 TEXT UNIQUE,
            供应商编号 TEXT,
            供应商名称 TEXT,
            供应商简称 TEXT,
            供应商类别 TEXT,
            费用类型 TEXT,
            付款金额 REAL,
            币种 TEXT DEFAULT 'CNY',
            付款状态 TEXT,
            发票号码 TEXT,
            开票日期 TEXT,
            付款到期日 TEXT,
            实际付款日期 TEXT,
            收款银行 TEXT,
            收款账户尾号 TEXT,
            申请部门 TEXT,
            申请人 TEXT,
            审批状态 TEXT,
            备注 TEXT
        )
    ''')
    
    applicants = [
        ('采购部', '张明'), ('采购部', '李华'), ('采购部', '王磊'),
        ('市场部', '陈晓'), ('市场部', '赵婷'),
        ('IT部', '刘强'), ('IT部', '周杰'),
        ('供应链部', '孙丽'), ('供应链部', '吴昊'),
        ('研发部', '郑博士'), ('研发部', '钱工'),
        ('售后部', '冯涛'), ('售后部', '褚芳'),
        ('财务部', '蒋敏'),
    ]
    
    banks_map = {
        '工商银行': '1023', '建设银行': '1589', '招商银行': '1367',
        '中国银行': '1053', '浦发银行': '1534', '南京银行': '8902'
    }
    
    for i, p in enumerate(payments):
        sup_code = p[0]
        sup = next(s for s in suppliers if s[0] == sup_code)
        pay_num = f"PAY-{20250001 + i}"
        
        invoice_date = p[4]
        due_date = p[5]
        paid_date = p[6]
        status = p[3]
        
        # Assign applicant
        dept_name = p[8]
        dept_applicants = [a for a in applicants if a[0] == dept_name]
        if not dept_applicants:
            dept_applicants = [('财务部', '蒋敏')]
        _, applicant = random.choice(dept_applicants)
        
        # Status logic
        if status == "已付款":
            approve = "已通过"
            inv = f"INV-{202500000 + i}"
        elif status in ("待审批", "审批中"):
            approve = "待审批"
            inv = f"FP-{202500000 + i}"
        elif status == "已逾期":
            approve = random.choice(["待审批", "已通过"])
            inv = f"FP-{202500000 + i}"
        else:
            approve = "待审批"
            inv = f"FP-{202500000 + i}"
        
        bank = p[7]
        c.execute('''
            INSERT INTO payments (
                付款编号, 供应商编号, 供应商名称, 供应商简称, 供应商类别,
                费用类型, 付款金额, 币种, 付款状态, 发票号码,
                开票日期, 付款到期日, 实际付款日期, 收款银行,
                收款账户尾号, 申请部门, 申请人, 审批状态, 备注
            ) VALUES (?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)
        ''', (
            pay_num, sup_code, sup[1], sup[2], sup[3],
            p[1], p[2], 'CNY', status, inv,
            invoice_date, due_date, paid_date, bank,
            banks_map.get(bank, '1000'), dept_name, applicant, approve, p[9]
        ))
    
    conn.commit()
    
    # Stats
    c.execute("SELECT COUNT(*), SUM(付款金额) FROM payments")
    total, amount = c.fetchone()
    c.execute("SELECT COUNT(*) FROM payments WHERE 付款状态='已付款'")
    paid = c.fetchone()[0]
    c.execute("SELECT COUNT(*) FROM payments WHERE 付款状态='已逾期'")
    overdue = c.fetchone()[0]
    
    conn.close()
    
    # Export CSV
    csv_path = os.path.join(os.path.dirname(__file__), "..", "data", "payments_real.csv")
    conn2 = sqlite3.connect(DB_PATH)
    c2 = conn2.cursor()
    c2.execute("SELECT * FROM payments")
    rows = c2.fetchall()
    col_names = [d[0] for d in c2.description]
    
    with open(csv_path, 'w', newline='', encoding='utf-8-sig') as f:
        w = csv.writer(f)
        w.writerow(col_names)
        w.writerows(rows)
    conn2.close()
    
    print(f"✅ 博西家电定制数据库")
    print(f"   {total} 条付款记录")
    print(f"   总金额: ¥{amount/100000000:.2f}亿")
    print(f"   已付款: {paid} 笔, 已逾期: {overdue} 笔")
    print(f"   供应商: {len(suppliers)} 家")
    print(f"   数据库: {DB_PATH}")

if __name__ == '__main__':
    init_db()
