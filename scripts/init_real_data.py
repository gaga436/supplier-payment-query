def init_db():
    if os.path.exists(DB_PATH):
        os.remove(DB_PATH)
    
    os.makedirs(os.path.dirname(DB_PATH), exist_ok=True)
    
    conn = sqlite3.connect(DB_PATH)
    c = conn.cursor()
    
    c.execute('''
        CREATE TABLE IF NOT EXISTS payments (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            付款编号 TEXT UNIQUE,
            供应商编号 TEXT,
            供应商名称 TEXT,
            供应商简称 TEXT,
            供应商类别 TEXT,
            费用类型 TEXT,
            付款金额 REAL,
            币种 TEXT DEFAULT 'CNY',
            付款状态 TEXT,
            发票号码 TEXT,
            开票日期 TEXT,
            付款到期日 TEXT,
            实际付款日期 TEXT,
            收款银行 TEXT,
            收款账户尾号 TEXT,
            申请部门 TEXT,
            申请人 TEXT,
            审批状态 TEXT,
            备注 TEXT
        )
    ''')
    
    applicants = ['刘洋', '陈静', '黄丽', '张伟', '王芳', '李强', '赵敏']
    banks_map = {
        '工商银行': '1023', '建设银行': '1589', '招商银行': '1367',
        '中国银行': '1053', '浦发银行': '1534', '建设银行': '1584'
    }
    
    for i, p in enumerate(payments):
        sup_code = p[0]
        sup = next(s for s in suppliers if s[0] == sup_code)
        pay_num = f"PAY-{20250001 + i}"
        
        fee_type = p[1]
        amount = p[2]
        status = p[3]
        invoice_date = p[4]
        due_date = p[5]
        paid_date = p[6]
        bank = p[7]
        dept = p[8]
        note = p[9]
        
        # Determine approval status and invoice number
        if status == "已付款":
            approve = "已通过"
            inv = f"INV-{202500000 + i}"
        elif status == "待审批" or status == "审批中":
            approve = "待审批"
            inv = f"FP-{202500000 + i}"
        elif status == "已逾期":
            approve = "待审批" if random.random() > 0.5 else "已通过"
            inv = f"FP-{202500000 + i}"
        else:
            approve = "待审批"
            inv = f"FP-{202500000 + i}"
        
        applicant = random.choice(applicants)
        
        c.execute('''
            INSERT INTO payments (
                付款编号, 供应商编号, 供应商名称, 供应商简称, 供应商类别,
                费用类型, 付款金额, 币种, 付款状态, 发票号码,
                开票日期, 付款到期日, 实际付款日期, 收款银行,
                收款账户尾号, 申请部门, 申请人, 审批状态, 备注
            ) VALUES (?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)
        ''', (
            pay_num, sup_code, sup[1], sup[2], sup[3],
            fee_type, amount, 'CNY', status, inv,
            invoice_date, due_date, paid_date, bank,
            banks_map.get(bank, '1000'), dept, applicant, approve, note
        ))
    
    conn.commit()
    conn.close()
    print(f"✅ 数据库初始化完成: {len(payments)} 条真实付款记录")
    print(f"   路径: {DB_PATH}")
    
    # Also generate CSV for reference
    csv_path = os.path.join(os.path.dirname(__file__), "..", "data", "payments_real.csv")
    conn = sqlite3.connect(DB_PATH)
    c = conn.cursor()
    c.execute("SELECT * FROM payments")
    rows = c.fetchall()
    col_names = [d[0] for d in c.description]
    
    with open(csv_path, 'w', newline='', encoding='utf-8-sig') as f:
        writer = csv.writer(f)
        writer.writerow(col_names)
        writer.writerows(rows)
    
    conn.close()
    print(f"✅ CSV 已导出: {csv_path} (含 {len(rows)} 条记录)")

if __name__ == '__main__':
    init_db()
